package com.tuplestores.admin.model;

public class Driver {
	
	private  String driver_id;
	
	private String email;
	
	private String name;
	
	private String mobile;
	
	private String driver_online;
	
	private String isd_code;
	
	private String first_name;
	
	private String last_name;
	
	private String checked_in_vehicle;
	
	private String verified;

	public String getDriver_id() {
		return driver_id;
	}

	public void setDriver_id(String driver_id) {
		this.driver_id = driver_id;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getDriver_online() {
		return driver_online;
	}

	public void setDriver_online(String driver_online) {
		this.driver_online = driver_online;
	}

	public String getChecked_in_vehicle() {
		return checked_in_vehicle;
	}

	public void setChecked_in_vehicle(String checked_in_vehicle) {
		this.checked_in_vehicle = checked_in_vehicle;
	}

	public String getVerified() {
		return verified;
	}

	public void setVerified(String verified) {
		this.verified = verified;
	}

	public String getIsd_code() {
		return isd_code;
	}

	public void setIsd_code(String isd_code) {
		this.isd_code = isd_code;
	}

	public String getFirst_name() {
		return first_name;
	}

	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}

	public String getLast_name() {
		return last_name;
	}

	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	
	
	
	
	

}
